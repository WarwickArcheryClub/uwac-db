    "round_id" INTEGER REFERENCES rounds,
    "bow_type" INTEGER REFERENCES bow_types,
    "gender" GENDER NOT NULL,
    "class_a" INTEGER DEFAULT NULL,
    "class_b" INTEGER DEFAULT NULL,
    "class_c" INTEGER DEFAULT NULL,
    "class_d" INTEGER DEFAULT NULL,
    "class_e" INTEGER DEFAULT NULL,
    "class_f" INTEGER DEFAULT NULL,
    "class_g" INTEGER DEFAULT NULL,
    "class_h" INTEGER DEFAULT NULL,
    PRIMARY KEY ("round_id", "bow_type", "gender")
);

